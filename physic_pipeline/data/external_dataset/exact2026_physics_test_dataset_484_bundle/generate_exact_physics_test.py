def fmt(x, max_dec=3):
    # deterministic, compact decimal formatting without scientific notation for contest-style answers
    if abs(x) < 1e-12:
        return '0'
    # avoid -0
    if abs(x - round(x)) < 1e-9:
        return str(int(round(x)))
    # choose decimals based on magnitude
    s = f"{x:.{max_dec}f}"
    s = s.rstrip('0').rstrip('.')
    if s == '-0': s = '0'
    return s

def add(category, question, answer, unit, cot, premises):
    # ensure unique; deterministic suffix unlikely needed
    global records
    if question in seen_questions:
        return False
    idx = len(records)+1
    rid = f"PHYTEST{idx:04d}"
    rec = {
        "id": rid,
        "source_record_id": rid,
        "type": "physics",
        "category": category,
        "question": question,
        "answer": str(answer),
        "unit": unit,
        "cot": cot,
        "explanation": cot,
        "premises": premises,
        "verification": "Generated by deterministic formula template and recomputed during dataset creation. Constants: k = 9 × 10^9 N·m²/C²; epsilon_0 = 8.854 × 10^-12 F/m."
    }
    records.append(rec)
    seen_questions.add(question)
    return True

